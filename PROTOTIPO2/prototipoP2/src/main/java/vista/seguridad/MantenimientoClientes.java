    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id_cliente");
        modelo.addColumn("nombre_cliente");
        modelo.addColumn("apellido_cliente");
        modelo.addColumn("edad_cliente");
        modelo.addColumn("correo_cliente");
        modelo.addColumn("direccion_cliente");
        modelo.addColumn("telefono_cliente");
        modelo.addColumn("dpi_cliente");
        ClientesDAO clienteDAO = new ClientesDAO();
        List<Clientes> cliente = clienteDAO.select();
        tablaAula.setModel(modelo);
        String[] dato = new String[8];
        for (int i = 0; i < cliente.size(); i++) {
            dato[0] = Integer.toString(cliente.get(i).getId_cliente());
            dato[1] = cliente.get(i).getNombre_cliente();
            dato[2] = cliente.get(i).getApellido_cliente();
            dato[3] = cliente.get(i).getEdad_cliente();
            dato[4] = cliente.get(i).getCorreo_cliente();
            dato[5] = cliente.get(i).getDireccion_cliente();
            dato[6] = cliente.get(i).getTelefono_cliente();
            dato[7] = cliente.get(i).getDpi_cliente();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

